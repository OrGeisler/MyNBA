class Player {
    id:string
    firstName: string;
    lastName: string;
    picture: string;
    position: string;
    jersey: string;

    constructor(
        id:string,
        firstName: string,
        lastName: string,
        picture: string,
        position: string,
        jersey: string
      )

      {
        this.id=id,
        this.firstName = firstName;
        this.lastName = lastName;
        this.picture = picture;
        this.position = position;
        this.jersey = jersey;
      }
    }

